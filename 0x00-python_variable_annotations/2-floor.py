#!/usr/bin/env python3
"""
This module contains a function 
to compute the floor of a float.
"""

import math

def floor(n: float) -> int:
    """
    Compute the floor of a float.
    
    Parameters:
    n (float): The number to compute the floor of.
    
    Returns:
    int: The floor of the float.
    """
    return math.floor(n)

